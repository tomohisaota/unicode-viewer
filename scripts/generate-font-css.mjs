#!/usr/bin/env node
// Regenerate src/app/cjk-ivs-font.css from the font chunks under
// public/fonts/. Each url() gets a `?v=<hash>` query string derived from a
// SHA-256 over every chunk's bytes, so any change to the bundled font
// busts browser caches automatically. Wired into the build via the
// `prebuild` npm script so a normal `next build` always ships an
// up-to-date CSS.
//
// CSS file shape and unicode-range conventions are preserved exactly
// so a no-op rebuild produces a no-op git diff.
import { readFileSync, readdirSync, writeFileSync } from "node:fs";
import { createHash } from "node:crypto";
import { join } from "node:path";

const FONT_DIR = "public/fonts";
const CSS_OUT = "src/app/cjk-ivs-font.css";
const VS_RANGE = "U+E0100-E01EF, U+FE00-FE0F";

const chunks = readdirSync(FONT_DIR)
  .filter((n) => /^cjk-ivs-[0-9A-F]{4}\.woff2$/.test(n))
  .sort();

if (chunks.length === 0) {
  console.error("no cjk-ivs-*.woff2 chunks found in public/fonts/");
  process.exit(1);
}

const hash = createHash("sha256");
for (const c of chunks) hash.update(readFileSync(join(FONT_DIR, c)));
const ver = hash.digest("hex").slice(0, 10);

const lines = [
  "/* CJK IVS font — Jigmo (IVD 2025-07-14, CC0) base + IVS, plus IPAmj明朝 SVS overlay */",
  "/* Covers IVS (E0100-E01EF) and SVS (FE00-FE0F) variation sequences */",
  "/* On-demand loading via CSS unicode-range. See /fonts/LICENSE.txt */",
  `/* Bundle hash: ${ver} — regenerated by scripts/generate-font-css.mjs */`,
  "",
];

for (const c of chunks) {
  const m = c.match(/^cjk-ivs-([0-9A-F]{4})\.woff2$/);
  const startHex = m[1];
  const startNum = parseInt(startHex, 16);
  const endNum = startNum + 0xff;
  const endHex = endNum.toString(16).toUpperCase().padStart(4, "0");
  lines.push(
    "@font-face {",
    "  font-family: 'CjkIvsMincho';",
    "  font-display: swap;",
    `  src: url('/fonts/${c}?v=${ver}') format('woff2');`,
    `  unicode-range: U+${startHex}-${endHex}, ${VS_RANGE};`,
    "}",
  );
}

writeFileSync(CSS_OUT, lines.join("\n") + "\n");
console.log(
  `generated ${CSS_OUT}: ${chunks.length} @font-face entries, v=${ver}`,
);
